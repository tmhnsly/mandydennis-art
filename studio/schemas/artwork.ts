import { defineType, defineField } from "sanity";

export default defineType({
  name: "artwork",
  title: "Gallery",
  type: "document",
  icon: () => "🎨",
  fields: [
    defineField({
      name: "title",
      title: "Title",
      type: "string",
      description: "Name of the piece, e.g. 'Bella — Pastel Portrait'",
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: "slug",
      title: "URL Slug",
      type: "slug",
      options: { source: "title", maxLength: 96 },
      description: "Tap the 'Generate' button to create this automatically from the title.",
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: "image",
      title: "Photo",
      type: "image",
      options: { hotspot: true },
      description: "Upload a photo of the artwork. Tap the image once uploaded to set the focus point — that's the part that stays visible when the photo is cropped.",
      fields: [
        defineField({
          name: "alt",
          title: "Description for screen readers",
          type: "string",
          description: "Optional but kind. A short description for people who can't see the image, e.g. 'Pastel portrait of a black labrador'.",
        }),
      ],
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: "description",
      title: "Description",
      type: "text",
      rows: 3,
      description: "Optional — a short note about this piece",
    }),
    defineField({
      name: "medium",
      title: "Medium",
      type: "array",
      of: [{ type: "string" }],
      description: "What was this made with?",
      options: {
        list: [
          { title: "Pastel", value: "pastel" },
          { title: "Watercolour", value: "watercolour" },
          { title: "Pencil", value: "pencil" },
          { title: "Oil", value: "oil" },
          { title: "Charcoal", value: "charcoal" },
          { title: "Mixed Media", value: "mixed media" },
          { title: "Acrylic", value: "acrylic" },
          { title: "Ink", value: "ink" },
        ],
      },
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: "subject",
      title: "Subject",
      type: "array",
      of: [{ type: "string" }],
      description: "What is this a picture of?",
      options: {
        list: [
          { title: "Pets", value: "pets" },
          { title: "Animals", value: "animals" },
          { title: "Dogs", value: "dogs" },
          { title: "Cats", value: "cats" },
          { title: "Horses", value: "horses" },
          { title: "Birds", value: "birds" },
          { title: "Wildlife", value: "wildlife" },
          { title: "People", value: "people" },
          { title: "Landscape", value: "landscape" },
          { title: "Seascapes", value: "seascapes" },
          { title: "Still Life", value: "still life" },
          { title: "Food", value: "food" },
          { title: "Flowers", value: "flowers" },
          { title: "Buildings", value: "buildings" },
        ],
      },
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: "featured",
      title: "Show on Homepage",
      type: "boolean",
      description: "Turn this on to show this piece in the featured row at the top of the homepage.",
      initialValue: false,
    }),
    defineField({
      name: "featuredOrder",
      title: "Featured order",
      type: "number",
      description: "Optional. Lower numbers show first on the homepage's featured row (e.g. 1 shows before 2). Leave blank to fall back to newest-first.",
      hidden: ({ parent }) => !parent?.featured,
    }),
    defineField({
      name: "date",
      title: "Date Added",
      type: "date",
      description: "When was this piece made? Auto-fills to today; tap to change if needed.",
      initialValue: () => new Date().toISOString().split("T")[0],
      validation: (rule) => rule.required(),
    }),
  ],
  orderings: [
    { title: "Newest First", name: "dateDesc", by: [{ field: "date", direction: "desc" }] },
    { title: "Title A-Z", name: "titleAsc", by: [{ field: "title", direction: "asc" }] },
  ],
  preview: {
    select: { title: "title", media: "image", subtitle: "date" },
  },
});
